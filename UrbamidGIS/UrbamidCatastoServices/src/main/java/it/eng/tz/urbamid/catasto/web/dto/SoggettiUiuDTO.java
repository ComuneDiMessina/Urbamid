package it.eng.tz.urbamid.catasto.web.dto;

public class SoggettiUiuDTO {

	private String nominativo;
	private String diritto;
	private String dataEfficacia;
	private String dataRegistrazione;
	private String tipoAtto;
	private String numeroAtto;
	private String progressivoAtto;
	private String annoAtto;
	private String descrizioneAtto;

	public String getNominativo() {
		return nominativo;
	}
	public void setNominativo(String nominativo) {
		this.nominativo = nominativo;
	}
	public String getDiritto() {
		return diritto;
	}
	public void setDiritto(String diritto) {
		this.diritto = diritto;
	}
	public String getDataEfficacia() {
		return dataEfficacia;
	}
	public void setDataEfficacia(String dataEfficacia) {
		this.dataEfficacia = dataEfficacia;
	}
	public String getDataRegistrazione() {
		return dataRegistrazione;
	}
	public void setDataRegistrazione(String dataRegistrazione) {
		this.dataRegistrazione = dataRegistrazione;
	}
	public String getTipoAtto() {
		return tipoAtto;
	}
	public void setTipoAtto(String tipoAtto) {
		this.tipoAtto = tipoAtto;
	}
	public String getNumeroAtto() {
		return numeroAtto;
	}
	public void setNumeroAtto(String numeroAtto) {
		this.numeroAtto = numeroAtto;
	}
	public String getProgressivoAtto() {
		return progressivoAtto;
	}
	public void setProgressivoAtto(String progressivoAtto) {
		this.progressivoAtto = progressivoAtto;
	}
	public String getAnnoAtto() {
		return annoAtto;
	}
	public void setAnnoAtto(String annoAtto) {
		this.annoAtto = annoAtto;
	}
	public String getDescrizioneAtto() {
		return descrizioneAtto;
	}
	public void setDescrizioneAtto(String descrizioneAtto) {
		this.descrizioneAtto = descrizioneAtto;
	}

}
